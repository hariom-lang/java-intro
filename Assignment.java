public class Assignment {
    public static void main (String [] args){
        int a=10;
    //  a = a + 10;
         a += 10;
         System.out.println(a);
    //  a = a - 10;  
         a -= 10;//value of a =20
         System.out.println(a); 
    //  a = a * 10;
         a *= 10;// value of a =10
         System.out.println(a); 
    //   a = a / 10;
         a /= 10;//value of a =100
         System.out.println(a);
    //  a = a % 10; 
         a %= 10;  //value of a =10  
        System.out.println(a);
    }

}
